uint8_t OLED_DisplayBuf[8][128];

void OLED_WriteData(uint8_t *Data,uint8_t Count)
{
	MyI2C_Start();
	MyI2C_SendByte(0x78); 	//寻址
	MyI2C_ReceiveAck();
	MyI2C_SendByte(0x40); 	//Control Byte
	MyI2C_ReceiveAck();
	for(uint8_t i=0; i < Count; i++)
	{
		MyI2C_SendByte(Data[i]); //Data Byte
		MyI2C_ReceiveAck();
	}
	MyI2C_Stop();
}

void OLED_WriteCommand(uint8_t Command)
{
	MyI2C_Start();
	MyI2C_SendByte(0x78); 	//寻址
	MyI2C_ReceiveAck();
	MyI2C_SendByte(0x00); 	//Control Byte
	MyI2C_ReceiveAck();
	MyI2C_SendByte(Command); //Data Byte
	MyI2C_ReceiveAck();
	MyI2C_Stop();
	
}



